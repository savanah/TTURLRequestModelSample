#import "TrendCell.h"


@implementation TrendCell

@synthesize trendObject = _trendObject;
@synthesize trendLabel = _trendLabel;

///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
#pragma mark -
#pragma mark TTTableViewCell

///////////////////////////////////////////////////////////////////////////////////////////////////
- (id)initWithStyle:(UITableViewCellStyle)style reuseIdentifier:(NSString*)identifier {
  if (self = [super initWithStyle:style reuseIdentifier:identifier]) {
    _trendLabel = [[UILabel alloc] init];
    [self.contentView addSubview:_trendLabel];
  }
  return self;
}

///////////////////////////////////////////////////////////////////////////////////////////////////
- (void)dealloc {
  TT_RELEASE_SAFELY(_trendObject);
  TT_RELEASE_SAFELY(_trendLabel);

  [super dealloc];
}

///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
#pragma mark -
#pragma mark UIView


///////////////////////////////////////////////////////////////////////////////////////////////////
- (void)layoutSubviews {
  [super layoutSubviews];

  _trendLabel.frame = CGRectMake(20, 3, self.bounds.size.width - 40, self.bounds.size.height - 6);
}

///////////////////////////////////////////////////////////////////////////////////////////////////
- (void)setObject:(id)object {
  if (self.trendObject != object) {
    self.trendObject = object;

    self.trendLabel.text = [object objectForKey:@"query"];
  }
}

@end
